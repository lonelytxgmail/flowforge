type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function readMessage(locale: Locale, key: string): TranslationValue | TranslationTree | undefined {
  return key.split(".").reduce<TranslationValue | TranslationTree | undefined>((current, part) => {
    if (!current || typeof current === "string" || typeof current === "function") {
      return undefined;
    }

    return current[part];
  }, messages[locale]);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>("zh-CN");

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, params) => {
        const message = readMessage(locale, key);

        if (typeof message === "function") {
          return message(params);
        }

        if (typeof message === "string") {
          return message;
        }

        return key;
      },
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}
